/**
 * Get Docker Compose services status
 * @param {string} basePath - Project base path
 * @returns {Promise<Object>} Status with running services
 */
async function getDockerServicesStatus(basePath) {
  const composeCmd = getDockerComposeCommand();
  try {
    const { stdout } = await execAsync(`${composeCmd} ps --format json`, {
      cwd: basePath,
      env: process.env
    });
    
    const services = JSON.parse(stdout);
    return {
      success: true,
      services,
      running: services.filter(s => s.State === 'running').length
    };
  } catch (err) {
    return {
      success: false,
      error: err.message,
      running: 0
    };
  }
}

/**
 * Get Docker container logs
 * @param {string} basePath - Project base path
 * @param {string} serviceName - Service name
 * @returns {Promise<Object>} Logs output
 */
async function getDockerLogs(basePath, serviceName) {
  try {
    const { stdout } = await execAsync(`docker logs ${serviceName}`, {
      cwd: basePath,
      env: process.env,
      maxBuffer: 1024 * 1024 // 1MB buffer
    });
    return {
      success: true,
      logs: stdout
    };
  } catch (err) {
    return {
      success: false,
      error: err.message
    };
  }
}

/**
 * Check if a project has Docker files
 * @param {string} basePath - Project base path
 * @returns {Promise<Object>} Docker file detection result
 */
async function hasDockerFiles(basePath) {
  const dockerfile = path.join(basePath, 'Dockerfile');
  const dockerCompose = path.join(basePath, 'docker-compose.yml');
  const dockerComposeOverride = path.join(basePath, 'docker-compose.override.yml');
  const dockerEnv = path.join(basePath, '.env');
  
  return {
    hasDockerfile: await fs.pathExists(dockerfile),
    hasDockerCompose: await fs.pathExists(dockerCompose),
    hasDockerComposeOverride: await fs.pathExists(dockerComposeOverride),
    hasDockerEnv: await fs.pathExists(dockerEnv)
  };
}

