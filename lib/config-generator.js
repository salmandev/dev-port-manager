const fs = require('fs').promises;
const path = require('path');

class ConfigGenerator {
  async writeFile(filePath, content, format = 'utf-8') {
    try {
      const dir = path.dirname(filePath);
      await fs.mkdir(dir, { recursive: true });
      await fs.writeFile(filePath, content, format);
    } catch (error) {
      throw new Error(`Failed to write file ${filePath}: ${error.message}`);
    }
  }

  generateProjectConfig(projectName, port, host) {
    return {
      name: projectName,
      host,
      port,
      url: `http://${host}:${port}`,
      protocol: 'http',
      timestamp: new Date().toISOString(),
      environment: {
        PROJ_NAME: projectName,
        PROJ_HOST: host,
        PROJ_PORT: port,
        PROJ_URL: `http://${host}:${port}`,
        LOCALHOST_PORT: port
      }
    };
  }

  async writeProjectConfig(outputDir, config) {
    const configPath = path.join(outputDir, '.project-dev.json');
    const content = JSON.stringify(config, null, 2);
    await this.writeFile(configPath, content);
    return configPath;
  }

  generateVSCodeSettings(projectPort) {
    const settings = {
      "[javascript]": {
        "editor.defaultFormatter": "esbenp.prettier-vscode",
        "editor.formatOnSave": true
      },
      "[json]": {
        "editor.defaultFormatter": "esbenp.prettier-vscode",
        "editor.formatOnSave": true
      },
      // Add port-specific settings
      "ports": [
        {
          "port": projectPort,
          "label": "Project Service",
          "onAutoForward": "notify"
        }
      ],
      "forwardPorts": [projectPort],
      "portsAttributes": {
        [projectPort]: {
          "label": "Project Service",
          "onAutoForward": "notify"
        }
      }
    };
    return settings;
  }

  async writeVSCodeSettings(outputDir, projectPort) {
    const vscodePath = path.join(outputDir, '.vscode', 'settings.json');
    const settings = this.generateVSCodeSettings(projectPort);

    // Check if settings.json already exists
    try {
      const existingContent = await fs.readFile(vscodePath, 'utf-8');
      const existingSettings = JSON.parse(existingContent);
      
      // Merge with existing settings
      const merged = {
        ...existingSettings,
        ...settings,
        forwardPorts: Array.from(
          new Set([
            ...(existingSettings.forwardPorts || []),
            ...settings.forwardPorts
          ])
        )
      };
      
      const content = JSON.stringify(merged, null, 2);
      await this.writeFile(vscodePath, content);
    } catch (error) {
      // File doesn't exist, create it
      const content = JSON.stringify(settings, null, 2);
      await this.writeFile(vscodePath, content);
    }

    return vscodePath;
  }

  generateDockerEnv(projectName, port, host) {
    return [
      `# Generated by Dev Port Manager`,
      `PROJ_NAME=${projectName}`,
      `PROJ_HOST=${host}`,
      `PROJ_PORT=${port}`,
      `PROJ_URL=http://${host}:${port}`,
      `LOCALHOST_PORT=${port}`,
      `# Service will be accessible at: http://${host}:${port}`
    ].join('\n');
  }

  async writeDockerEnv(outputDir, projectName, port, host) {
    const envPath = path.join(outputDir, '.env');
    const content = this.generateDockerEnv(projectName, port, host);

    // Append to existing .env if present
    try {
      const existingContent = await fs.readFile(envPath, 'utf-8');
      const mergedContent = existingContent + '\n\n' + content;
      await this.writeFile(envPath, mergedContent);
    } catch (error) {
      // File doesn't exist, create it
      await this.writeFile(envPath, content);
    }

    return envPath;
  }

  generateBootstrapScript(projectName, outputDir = null) {
    // Bash version (Unix/Linux/Mac/WSL)
    const bashScript = `#!/bin/bash
# Bootstrap script for ${projectName}
# Auto-generated by Dev Port Manager

set -e

PROJECT_NAME="${projectName}"
OUTPUT_DIR="\${OUTPUT_DIR:-.}"

echo "🚀 Bootstrapping $PROJECT_NAME..."

# Call global CLI
if command -v dpm &> /dev/null; then
    dpm assign "$PROJECT_NAME" --output "$OUTPUT_DIR" --vscode
else
    echo "❌ dev-port-manager CLI not found"
    echo "Install with: npm install -g @devops/dev-port-manager"
    exit 1
fi

# Source the config
if [ -f "$OUTPUT_DIR/.project-dev.json" ]; then
    echo "✓ Configuration generated at $OUTPUT_DIR/.project-dev.json"
fi

echo "✓ Bootstrap complete!"
echo ""
echo "Project configuration:"
cat "$OUTPUT_DIR/.project-dev.json" 2>/dev/null || echo "  (config file not found)"
`;

    // PowerShell version (Windows)
    const psScript = `# Bootstrap script for ${projectName}
# Auto-generated by Dev Port Manager

param(
    [string]$OutputDir = "."
)

$ProjectName = "${projectName}"

Write-Host "🚀 Bootstrapping $ProjectName..." -ForegroundColor Green

# Call global CLI
$dpmCmd = Get-Command dpm -ErrorAction SilentlyContinue

if ($dpmCmd) {
    & dpm assign $ProjectName --output $OutputDir --vscode
} else {
    Write-Host "❌ dev-port-manager CLI not found" -ForegroundColor Red
    Write-Host "Install with: npm install -g @devops/dev-port-manager" -ForegroundColor Yellow
    exit 1
}

# Display config
$configFile = Join-Path $OutputDir ".project-dev.json"
if (Test-Path $configFile) {
    Write-Host "✓ Configuration generated at $configFile" -ForegroundColor Green
    Write-Host ""
    Write-Host "Project configuration:" -ForegroundColor Cyan
    Get-Content $configFile | ConvertFrom-Json | Format-List
}

Write-Host "✓ Bootstrap complete!" -ForegroundColor Green
`;

    return { bash: bashScript, powershell: psScript };
  }

  async writeBootstrapScripts(outputDir, projectName) {
    const scripts = this.generateBootstrapScript(projectName);

    // Write bash script
    const bashPath = path.join(outputDir, 'bootstrap.sh');
    await this.writeFile(bashPath, scripts.bash);
    // Make executable on Unix (can't be done directly on Windows)

    // Write PowerShell script
    const psPath = path.join(outputDir, 'bootstrap.ps1');
    await this.writeFile(psPath, scripts.powershell);

    return { bash: bashPath, powershell: psPath };
  }

  generateReadme(projectConfig) {
    return `# ${projectConfig.name}

## Project Details

- **Host**: ${projectConfig.host}
- **Port**: ${projectConfig.port}
- **URL**: [${projectConfig.url}](${projectConfig.url})
- **Environment**: ${projectConfig.environment.PROJ_NAME}

## Environment Variables

\`\`\`bash
PROJ_NAME=${projectConfig.environment.PROJ_NAME}
PROJ_HOST=${projectConfig.environment.PROJ_HOST}
PROJ_PORT=${projectConfig.environment.PROJ_PORT}
PROJ_URL=${projectConfig.environment.PROJ_URL}
LOCALHOST_PORT=${projectConfig.environment.LOCALHOST_PORT}
\`\`\`

## Quick Start

### Local Development

\`\`\`bash
# Your service should listen on 127.0.0.1:${projectConfig.port}
# Access it at ${projectConfig.url}
\`\`\`

### Docker Compose

\`\`\`yaml
services:
  ${projectConfig.name}:
    ports:
      - "${projectConfig.port}:8080"  # Adjust internal port as needed
    environment:
      - PORT=${projectConfig.port}
\`\`\`

## Configuration File

See [.project-dev.json](.project-dev.json) for auto-generated configuration details.

Generated by [Dev Port Manager](https://github.com/dev-port-manager)
`;
  }

  async writeReadme(outputDir, projectConfig) {
    const readmePath = path.join(outputDir, 'PROJECT_DEV.md');
    const content = this.generateReadme(projectConfig);
    await this.writeFile(readmePath, content);
    return readmePath;
  }
}

module.exports = ConfigGenerator;
